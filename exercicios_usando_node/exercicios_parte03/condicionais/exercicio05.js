/*Faça um programa que leia um número e informe se ele é positivo, negativo ou zero */

const numero = 0

if (numero > 0) {
    console.log(`O número ${numero} é positivo`)
} else if (numero < 0) {
    console.log(`O número ${numero} é negativo`)
} else {
    console.log('Você digitou o número 0')
}